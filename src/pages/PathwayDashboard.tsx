// ─── Sortable Sidebar Item (assigned — full-card draggable) ───────────────────

interface SortableSidebarItemProps {
  pathwayId: string;
  label: string;
  isSelected: boolean;
  matchScore?: number | null;
  fitScore?: number | null;
  onClick: () => void;
  onRemove: (pathwayId: string) => void;
}

function SortableSidebarItem({ pathwayId, label, isSelected, matchScore, fitScore, onClick, onRemove }: SortableSidebarItemProps) {
  const { attributes, listeners, setNodeRef, transform, transition, isDragging } = useSortable({ id: pathwayId });
  const [hovered, setHovered] = useState(false);

  const style: React.CSSProperties = {
    transform: CSS.Transform.toString(transform),
    transition: transition ?? 'transform 200ms cubic-bezier(0.25, 1, 0.5, 1)',
    opacity: isDragging ? 0.45 : 1,
    boxShadow: isDragging ? '0 6px 20px rgba(0,0,0,0.18)' : 'none',
    zIndex: isDragging ? 999 : undefined,
    display: 'flex',
    alignItems: 'center',
    borderRadius: 'var(--radius-md)',
    userSelect: 'none',
    touchAction: 'none',
    cursor: isDragging ? 'grabbing' : 'grab',
    border: isSelected ? '1px solid var(--brand-500)' : '1px solid transparent',
    background: isSelected ? 'rgba(154,184,46,0.08)' : hovered && !isDragging ? 'var(--surface)' : 'transparent',
    padding: '0.5rem 0.375rem',
    gap: '0.375rem',
  };

  return (
    <div
      ref={setNodeRef}
      style={style}
      {...attributes}
      {...listeners}
      onClick={onClick}
      onMouseEnter={() => setHovered(true)}
      onMouseLeave={() => setHovered(false)}
    >
      {/* Drag hint — visual only, no pointer events */}
      <span style={{
        color: 'var(--text-faint)',
        fontSize: '0.7rem',
        flexShrink: 0,
        lineHeight: 1,
        opacity: hovered ? 0.5 : 0.2,
        transition: 'opacity 0.15s',
        pointerEvents: 'none',
      }}>⠿</span>
      <span style={{ fontSize: '0.6rem', color: 'var(--brand-500)', flexShrink: 0, lineHeight: 1, pointerEvents: 'none' }}>●</span>
      <span style={{
        fontSize: 'var(--text-xs)',
        fontWeight: isSelected ? 700 : 600,
        color: isSelected ? 'var(--brand-700)' : 'var(--text-strong)',
        overflow: 'hidden', textOverflow: 'ellipsis', whiteSpace: 'nowrap', flex: 1,
        pointerEvents: 'none',
      }}>
        {label}
      </span>
      {(matchScore != null || fitScore != null) && (
        <span style={{ fontSize: '0.65rem', fontWeight: 700, color: matchScore != null ? matchColor(matchScore) : 'var(--text-muted)', flexShrink: 0, pointerEvents: 'none' }}>
          {matchScore != null ? `${matchScore}%` : fitScore != null ? `${fitScore}%` : ''}
        </span>
      )}
      {/* Remove button — stopPropagation prevents drag and click-select */}
      <button
        type="button"
        onPointerDown={e => e.stopPropagation()}
        onClick={e => { e.stopPropagation(); onRemove(pathwayId); }}
        style={{
          opacity: hovered && !isDragging ? 0.55 : 0,
          transition: 'opacity 0.15s',
          flexShrink: 0,
          background: 'transparent',
          border: 'none',
          cursor: 'pointer',
          color: 'var(--text-faint)',
          fontSize: '0.7rem',
          padding: '2px 5px',
          lineHeight: 1,
          borderRadius: 'var(--radius-sm)',
        }}
        title="Remove from My Pathways"
      >
        ✕
      </button>
    </div>
  );
}

