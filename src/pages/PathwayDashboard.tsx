// ─── Helpers ──────────────────────────────────────────────────────────────────

function matchColor(pct: number | null) {
  if (pct === null) return 'var(--text-muted)';
  if (pct >= 70) return 'var(--success, #15803d)';
  if (pct >= 45) return 'var(--warning, #b45309)';
  return 'var(--error, #dc2626)';
}

function readinessLabel(level: string | null, pct: number | null): string {
  if (level === 'strong_candidate') return 'Strong candidate';
  if (level === 'ready') return 'Ready to apply';
  if (level === 'building') return 'Building readiness';
  if (level === 'early_stage') return 'Early stage — keep building';
  if (pct !== null) {
    if (pct >= 70) return 'Strong candidate';
    if (pct >= 45) return 'Building readiness';
    return 'Early stage — keep building';
  }
  return '';
}

